#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	if (n < 0)
	{
		ft_putchar(45);
		n *= -1;
	}
	if (n > 9)
		ft_putnbr(n / 10);
	n %= 10;
	ft_putchar(n + 48);
}
